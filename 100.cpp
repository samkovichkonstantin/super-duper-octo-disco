#include <iostream>
#include <cmath>
#define PI 3.14159265358979323846

using namespace std;

int main() {
    // Данные
    double m_b = 0.02;                // Погрешность базисной стороны (м)
    double m_d = 0.08;                // Погрешность измеренной стороны (м)
    double m_alpha_sec = 2.0;         // Погрешность измерения угла (в секундах)
    double d;                         // Длина стороны треугольника (м)

    // Ввод длины стороны треугольника
    cout << "Введите длину стороны треугольника (м): ";
    cin >> d;

    // Перевод угловой погрешности в радианы
    double m_alpha_rad = m_alpha_sec * PI / 648000.0;

    // Вычисление средней квадратической погрешности неприступного расстояния
    double m_D = sqrt(pow(m_d, 2) + pow((d / sqrt(3)) * m_alpha_rad, 2));

    // Вывод результата
    cout << "Средняя квадратическая погрешность неприступного расстояния: " << m_D << " м" << endl;

    return 0;
}
