#include <iostream>
#include <cmath>
using namespace std;

int main() {
    // Данные
    double m1 = 4.0;     // Средняя квадратическая погрешность из 3 приемов (в секундах)
    int n1 = 3;          // Количество приемов для m1
    int n2 = 5;          // Новое количество приемов

    // Вычисление средней квадратической погрешности для 5 приемов
    double m2 = m1 * sqrt(static_cast<double>(n1) / n2);

    // Вывод результата
    cout << "Средняя квадратическая погрешность для " << n2 << " приемов: " << m2 << " секунд" << endl;

    return 0;
}
