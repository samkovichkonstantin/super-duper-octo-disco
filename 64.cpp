#include <iostream>
#include <cmath>
using namespace std;

int main() {
    // Входные данные
    int n = 9;                  // Количество измеренных углов
    double m = 20.0;            // Средняя квадратическая погрешность измерения одного угла (в секундах)

    // Вычисление допустимой величины угловой невязки
    double f = sqrt(n) * m;

    // Вывод результата
    cout << "Допустимая величина угловой невязки: " << f << " секунд" << endl;

    return 0;
}
